int main(int argc, char* argv[])
{
	AckurCHAR appDir;
	appDir.FROM(argv[0]);
	appDir.FROM(argv[0]);
	while (appDir.mLength && appDir.mBytes[appDir.mLength - 1] != '/' && appDir.mBytes[appDir.mLength - 1] != '\\')
		appDir.mLength--;
	appDir.mBytes[appDir.mLength] = 0;

	AckurError err = main_init(appDir);
	if (err)
	{
		gSystem->PrintLine(AckurCHAR("Failed to start."));
		gSystem->GetChar();
		return(0);
	}

	AckurFileIO f;
	if (f.Open("config.ackdb"))
	{
		AckurCHAR l;
		license(l);
		AckurZlib::Expand(l);
		gSystem->PrintLine(l);
		f.Delete("LICENSE.TXT");
		AckurError err = f.Create("LICENSE.TXT");
		if (!err)
		{
			f.Write(0, 0, 0, l.mLength, l.mBytes);
		}
		f.Close();
		l.FROM("\r\nPlease read the above license agreement carefully.\r\nEnter ");
		AckurPrime p;
		p.genRandomBits(12);
		AckurCHAR cmp(p.ToString(16));
		l.Append(cmp);
		l.Append(" to acknowledge agreement: ");
		gSystem->Print(l);
		l.SetLength(0);
		int ch;
		while (((ch = gSystem->GetChar()) != EOF) && (ch != '\n') && (ch != '\r'))
			l.Append((char)ch);
		if (l.StrUpperCmp(cmp))
		{
			gSystem->PrintLine(AckurCHAR("\r\nCode entered does not match.\r\nPress enter to exit: "));
			while (((ch = gSystem->GetChar()) != EOF) && (ch != '\n'))
				;
			return(0);
		}
		gSystem->Print("\r\nAgreement has been acknowledged. Continuing with setup...");
		if (f.Open("LICENSE.TXT"))
		{
			l.FROM("\r\nERROR: The Ackur software needs to be run from a location where\r\n");
			l.Append("files can be created. Please move the application and try again. Press enter to exit: ");
			gSystem->PrintLine(l);
			while (((ch = gSystem->GetChar()) != EOF) && (ch != '\n'))
				;
			return(0);
		}
		if (f.Open("init.ackscript"))
		{
			AckurBYTE l;
			init(l);
			AckurZlib::Expand(l);
			AckurError err = f.Create("init.ackscript");
			if (!err)
			{
				f.Write(0, 0, 0, l.mLength, l.mBytes);
			}
			f.Close();
		}
	}

	AckurCHAR in;

	if (argc > 1)
	{
		in.FROM(argv[1]);
		AckurCHAR argName("arg");
		bool wasCreated;
		AckurRuleParameterEvent* argParam = gSystem->vConsoleConnect->vTokenState->CreateVariable(argName, wasCreated);
		argParam->vQueue = new AckurRuleParameterQueue();
		for (int i = 0; i < argc; i++)
		{
			AckurCHAR a(argv[i]);
			AckurRuleParameterEvent* arg = new AckurRuleParameterEvent(AckurBYTE::STR, i, "", argv[i]);
			argParam->vQueue->PushTail(arg);
		}
	}
	else
	{
		AckurFileIO f;
		AckurError err = f.Open("init.ackscript");
		if (!err)
		{
			u64 size = 0;
			err = f.GetSize(size);
			if (!err && size)
			{
				in.SetLength(size, false);
				err = f.Read(0, 1, 0, size, in.mBytes);
				if (err)
					in.SetSize(0);
			}
		}
	}

	if (in.mLength)
	{
		AckurTokenExec tokenExec;
		tokenExec.vResponse = gSystem->vConsoleConnect->vTokenState->vResponse;
		tokenExec.vTokenState = gSystem->vConsoleConnect->vTokenState;
		gSystem->vConsoleConnect->vTokenState->WaitCritical();
		gSystem->vConsoleConnect->vTokenState->vResponse->isExec = true;
		AckurRuleParameterEvent* result = tokenExec.Exec(0,in);
		gSystem->vConsoleConnect->vTokenState->vResponse->isExec = false;
		gSystem->vConsoleConnect->vTokenState->LeaveCritical();
		gSystem->vConsoleConnect->vTokenState->UpdateResult(result);
		gSystem->vConsoleConnect->vTokenState->mNeedsPrompt = true;
	}
	in.SetSize(0);

	if (!gSystem->mStop)
		gSystem->vConsoleConnect->vTokenState->SendPrompt();

	while (!gSystem->mStop)
	{
		AckurCHAR sendBuffer;
		int ch,lastch;

		do {
			sendBuffer.SetLength(0);
			lastch = 0;
			while (((ch = gSystem->GetChar()) != EOF) && (ch != '\n') && (ch != '\r'))
			{
				sendBuffer.Append((char)ch);
				lastch = ch;
			}
			if (sendBuffer.mLength == 1 && sendBuffer.mBytes[0] == '.') { gSystem->mStop = true; break; }
			if (sendBuffer.mLength == 1 && sendBuffer.mBytes[0] == '>') { break; }
			if (sendBuffer.mLength)
			{
				sendBuffer.Append("$\n");
				err = gSystem->vConsoleConnect->Send(sendBuffer.mBytes, sendBuffer.mLength);
			}
		} while (!gSystem->mStop && (sendBuffer.mLength || ch == '\n' || ch == '\r'));


		if (!gSystem->mStop)
		{
			gSystem->vConsoleConnect->Stop();
			err = gSystem->vConsoleConnect->Start();
			if (err)
			{
				gSystem->mStop = true;
				break;
			}
		}
	}

	gSystem->vConsoleConnect->Stop();
	gSystem = NULL;
	return 0;
}

