	u64 mac;
	char ipx[256];
	char namex[256];
	AckurNet::GetMac(namex, ipx, mac);

	time(&gSystem->mStartTime);
	gSystem->mStartClock = (clock() * 1000) / CLOCKS_PER_SEC;

	gSystem->mCode.FROM("hello");

	AckurError err;
	AckurCHAR machine("console"), path(""), file("config.ackdb");

	AckurConsoleConnectEvent* cxonsole = new AckurConsoleConnectEvent(machine, path, file);
	gSystem->mConsoleConnectQueue.PushTail(cxonsole);
	gSystem->vConsoleConnect = &cxonsole->mConsoleConnect;

